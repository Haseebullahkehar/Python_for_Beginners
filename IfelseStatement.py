"""
IF else Statement:
    If the condition is true, the if block code is executed and
    if the condition is false, the else block code is executed.

Syntax:
if condition:
    # block of code if condition is True

else:
    # block of code if condition is False

Example:
"""
# Example:01
if True:
    print("I run because if is true")
else:
    print("I run because if is false")

# Example:02
if False:
    print("I run because if is true")
else:
    print("I run because if is false")


# Example:03
number = 10

if number > 0:
    print('Positive number')
else:
    print('Negative number')
# OUTPUT: Positive number


# We can use if else statement in loops as well.
